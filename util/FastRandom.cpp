#include "FastRandom.hpp"
#include <cmath>
// -------------------------------------------------------------------------------------
using namespace std;
// -------------------------------------------------------------------------------------
namespace unv {
// -------------------------------------------------------------------------------------
FastRandom::FastRandom(ub8 seed)
        : seed(seed)
{
}
// -------------------------------------------------------------------------------------
ub4 FastRandom::rand()
{
   seed ^= (seed << 13);
   seed ^= (seed >> 15);
   return (ub4) (seed ^= (seed << 5));
}
// -------------------------------------------------------------------------------------
sb4 FastRandom::srand()
{
   return (sb4) rand();
}
// -------------------------------------------------------------------------------------
ub4 FastRandom::rand(ub4 min, ub4 max)
{
   return min + rand() % (max - min + 1);
}
// -------------------------------------------------------------------------------------
float FastRandom::randf(float min, float max)
{
   return min + ScaleFactor() * (max - min);
}
// -------------------------------------------------------------------------------------
sb4 FastRandom::srand(sb4 min, sb4 max)
{
   return min + rand() % (max - min + 1);
}
// -------------------------------------------------------------------------------------
float FastRandom::ScaleFactor()
{
   return static_cast<float>(rand()) / numeric_limits<ub4>::max();
}
// -------------------------------------------------------------------------------------
bool FastRandom::Bool()
{
   return rand() % 2 == 0;
}
// -------------------------------------------------------------------------------------
}
